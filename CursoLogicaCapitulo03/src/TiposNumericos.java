
public class TiposNumericos {
	
	public static void main(String[] args) {
		
		Short numeroShort = 1; // -32768 ate 32767
		
		Integer  numeroInteger = 1; // -2147483648 ate 2147483647
		
		Long numeroLong = 1L; // - 9,223372037*101 ate 9,223372037*10^1
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0;
		
		System.out.println(numeroDouble + numeroFloat + numeroInteger + numeroLong + numeroShort);
		
	}
}
